#unions
x = {"apple","orange","banana"}
y = {"pink","blue","apple"}

z = x.union(y)
print(z)

#Append
fruits = ['apple','banana','orange']
fruits.append("kiwi")
print(fruits)

#pop
fruits = ['apple','banana','orange']
fruits.pop(1)
print(fruits)
fruits.pop(0)
print(fruits)


#insert

fruits = ['apple','banana','orange']
fruits.insert(1,"mango")
print(fruits)
fruits.insert(6,"avacado")
print(fruits)



#sort
fruits = ['banana','orange','apple']
fruits.sort()
print(fruits)


numbers = ['3','7','1']
numbers.sort()
